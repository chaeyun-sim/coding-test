function solution(n) {
    const dp = new Array(n + 1).fill(0)
    
    dp[1] = 1
    dp[2] = 2
    
    for (let i = 3; i <= n; i++) {
        dp[i] = (dp[i - 1] + dp[i - 2]) % 1234567
    }
    
    return dp[n]
}

// 1 -> 1 (1)
// 2 -> 1+1, 2 (2)
// 3 -> 1+1+1, 2+1, 1+2, (3)
// 4 -> 1+1+1+1, 2+1+1, 1+2+1, 1+1+2, 2+2 (5)
// 5 -> 1+1+1+1+1, 2+2+1, 2+1+2, 1+2+2, 2+1+1+1, 1+2+1+1, 1+1+2+1, 1+1+1+2 (8)